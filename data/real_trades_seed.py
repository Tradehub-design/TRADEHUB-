def load_real_trades():
    columns = [
        "ticket",
        "symbol",
        "direction",
        "net_profit",
        "open_time",
        "trade_date",
        "entry_price",
        "exit_price",
        "volume",
        "swap",
        "balance",
    ]

    df = pd.DataFrame(
        REAL_TRADES,
        columns=columns
    )

    df["account_number"] = "real_sample"
    df["commission"] = 0.0
    df["source"] = "Real Trade Seed"

    df["session"] = pd.to_datetime(
        df["trade_date"],
        errors="coerce"
    ).dt.hour.apply(assign_session)

    return df


def assign_session(hour):
    try:
        hour = int(hour)
    except Exception:
        return "Unknown"

    if 0 <= hour < 7:
        return "Asia"

    if 7 <= hour < 16:
        return "London"

    if 16 <= hour < 22:
        return "New York"

    return "Asia"
